package com.tmarsteel.jcli;

import com.tmarsteel.jcli.command.NoSuchCommandException;

/**
 * Thrown whenever an input element (flag, option, argument) is to be accessed by name that is not known to the object
 * that is being asked for that element.
 */
public class NoSuchElementException extends RuntimeException
{
    public NoSuchElementException(String message) {
        super(message);
    }

    public NoSuchElementException(String message, Throwable cause) {
        super(message, cause);
    }
}
